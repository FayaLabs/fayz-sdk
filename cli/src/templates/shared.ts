// Files every generated app shares, regardless of surface kind. The shapes are
// copied from the dogfood apps (pulse-store / beauty-saas / course-members) —
// they are the proven "compiles against published npm" baseline.

export type Kind = 'storefront' | 'admin' | 'member'

export interface AppTemplate {
  kind: Kind
  /** Dev/preview port, matching the dogfood port lanes. */
  port: number
  /** @fayz-ai packages — versions resolved from the release channel. */
  fayzDependencies: string[]
  /** Non-fayz runtime deps with their pinned ranges. */
  externalDependencies: Record<string, string>
  files(name: string): Record<string, string>
  /** Kind-specific personalization checklist, embedded in CLAUDE.md. */
  checklist: string
  note?: string
}

/** kebab-case app name → display title ("minha-loja" → "Minha Loja"). */
export function titleCase(name: string): string {
  return name
    .split('-')
    .filter(Boolean)
    .map((w) => w[0].toUpperCase() + w.slice(1))
    .join(' ')
}

