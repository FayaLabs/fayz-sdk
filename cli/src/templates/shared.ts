export function viteConfig(port: number): string {
  return `import { defineConfig } from 'vite'
import react from '@vitejs/plugin-react'
import { fayzVite } from '@fayz-ai/sdk/vite'

// fayzVite resolves @fayz-ai/* from local SDK source when the fayz-sdk repo is
// checked out two levels up; everywhere else (including this app's default
// location) it falls back to the published npm packages in node_modules.
export default defineConfig(fayzVite({ port: ${port}, plugins: [react()] }))
`
}

