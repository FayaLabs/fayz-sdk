function packageJson(name: string, kind: Kind): string {
  const deps: Record<string, string> = {
    ...resolveFayzPackageDependencies('stable'),
    react: '^18.3.0',
    'react-dom': '^18.3.0',
  }
  for (const dep of TEMPLATES[kind].extraDeps) deps[dep] = RUNTIME_VERSION
  return (
    JSON.stringify(
      {
        name,
        private: true,
        version: '0.1.0',
        type: 'module',
        scripts: { dev: 'vite', build: 'vite build', preview: 'vite preview', doctor: 'fayz doctor' },
        dependencies: deps,
        devDependencies: {
          '@fayz/cli': RUNTIME_VERSION,
          '@vitejs/plugin-react': '^4.3.0',
          typescript: '^5.5.0',
          vite: '^5.4.0',
        },
      },
      null,
      2,
    ) + '\n'
  )
}

function manifestJson(name: string, kind: Kind): string {
  return JSON.stringify(TEMPLATES[kind].manifest(name), null, 2) + '\n'
}

function mainTsx(surface: string): string {
  return `import React from 'react'
import { createRoot } from 'react-dom/client'
import { renderApp } from '@fayz-ai/runtime'
import '@fayz-ai/runtime/styles.css'
import './styles.css'
import './plugins.generated' // registers plugin factories + scaffolds + providers
import './registry' // app-local custom blocks / components / overrides
import manifest from '../app.manifest.json'

createRoot(document.getElementById('root')!).render(
  <React.StrictMode>{renderApp(manifest as never, { surface: '${surface}' })}</React.StrictMode>,
)
`
}

function registryTsx(): string {
  return `// App-local custom code (customization ladder levels 5–7). Register custom
// blocks, pages, components and metric overrides here; reference them from
// app.manifest.json by their 'custom:' id.
//
//   import { registerBlock } from '@fayz-ai/runtime'
//   registerBlock('custom:my-block', MyBlock)
export {}
`
}

function viteConfig(): string {
  return `import { defineConfig } from 'vite'
import react from '@vitejs/plugin-react'

export default defineConfig({
  plugins: [react()],
  // Generated apps consume published @fayz/* packages — no source aliases.
})
`
}

function tsconfig(): string {
  return (
    JSON.stringify(
      {
        compilerOptions: {
          target: 'ES2020',
          lib: ['ES2020', 'DOM', 'DOM.Iterable'],
          module: 'ESNext',
          moduleResolution: 'bundler',
          jsx: 'react-jsx',
          strict: true,
          skipLibCheck: true,
          resolveJsonModule: true,
          noEmit: true,
        },
        include: ['src', 'app.manifest.json'],
      },
      null,
      2,
    ) + '\n'
  )
}

function indexHtml(name: string): string {
  return `<!doctype html>
<html lang="pt-BR">
  <head>
    <meta charset="UTF-8" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <title>${name}</title>
  </head>
  <body>
    <div id="root"></div>
    <script type="module" src="/src/main.tsx"></script>
  </body>
</html>
`
}

export function create(kind: string, name: string): number {
  if (!KINDS.includes(kind as Kind)) {
    console.error(`✗ Unknown kind "${kind}". Use: ${KINDS.join(' | ')}`)
    return 1
  }
  if (!name || !/^[a-z0-9][a-z0-9-]*$/.test(name)) {
    console.error(`✗ Provide a kebab-case app name, e.g. fayz create ${kind} my-app`)
    return 1
  }
  const root = resolve(process.cwd(), name)
  if (existsSync(root)) {
    console.error(`✗ Directory "${name}" already exists.`)
    return 1
  }
  const k = kind as Kind
  const template = TEMPLATES[k]

  write(root, 'app.manifest.json', manifestJson(name, k))
  write(root, 'package.json', packageJson(name, k))
  write(root, '.gitignore', 'node_modules\ndist\n.env\n.env.*\n!.env.example\n.DS_Store\n')
  write(root, 'index.html', indexHtml(name))
  write(root, 'vite.config.ts', viteConfig())
  write(root, 'tsconfig.json', tsconfig())
  write(root, 'src/main.tsx', mainTsx(template.surface))
  write(root, 'src/plugins.generated.ts', template.pluginsGenerated())
  write(root, 'src/registry.tsx', registryTsx())
  write(root, 'src/styles.css', '/* app-local styles */\n')
  write(
    root,
    'README.md',
    `# ${name}\n\nA Fayz ${k} app. The app uses the public npm \`@fayz-ai/sdk\` package for API/runtime helpers, \`@fayz-ai/runtime\` for manifest rendering, and keeps custom code in \`src/registry.tsx\`.\n\n\`\`\`bash\nnpm install\nnpm run dev\nnpm run doctor\n\`\`\`\n`,
  )

  console.log(`✓ Created ${k} app "${name}"`)
  console.log(`\n  cd ${name}`)
  console.log('  npm install')
  console.log('  npm run dev')
  if (template.note) console.log(`\n  Note: ${template.note}`)
  return 0
}
