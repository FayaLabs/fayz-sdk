import { mkdirSync, writeFileSync, existsSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { resolveFayzPackageDependencies, resolveFayzPackageVersion } from '../lib/package-versions.js'

type Kind = 'storefront' | 'admin' | 'member'

const KINDS: Kind[] = ['storefront', 'admin', 'member']

function write(root: string, rel: string, content: string): void {
  const full = resolve(root, rel)
  mkdirSync(dirname(full), { recursive: true })
  writeFileSync(full, content)
}

const RUNTIME_VERSION = resolveFayzPackageVersion('@fayz-ai/runtime')

// ---------------------------------------------------------------------------
// Per-kind template descriptor — the single source the generators read so a new
// surface type is one table entry, not a fork of every function.
// ---------------------------------------------------------------------------
interface KindTemplate {
  /** Extra runtime-versioned deps beyond @fayz-ai/runtime + react. */
  extraDeps: string[]
  manifest(name: string): unknown
  /** Body of src/plugins.generated.ts. */
  pluginsGenerated(): string
  /** Surface name to mount (renderApp surface). */
  surface: string
  note?: string
}

