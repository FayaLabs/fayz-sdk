#!/usr/bin/env node
// Resolve Unsplash imagery for storefront apps at BUILD TIME.
//
//   UNSPLASH_ACCESS_KEY=xxx node scripts/fetch-unsplash.mjs ../shopfront ../tannat-store ...
//
// Reads <app>/src/images.manifest.mjs and writes <app>/src/images.generated.ts
// containing plain images.unsplash.com CDN URLs (hotlinking — no runtime API
// calls, no key in the client bundle).
//
// Manifest entries:
//   { key: 'hero-1', query: 'vineyard sunset', w: 1800, h: 900 }            // one photo
//   { keys: ['a','b'], query: 'wine bottle', w: 900, h: 900 }               // one search, distributed
//
// Grouped entries keep us far below the 50 req/h demo rate limit.

import { readFileSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'
import { pathToFileURL } from 'node:url'

const ACCESS_KEY = process.env.UNSPLASH_ACCESS_KEY
if (!ACCESS_KEY) {
  console.error('UNSPLASH_ACCESS_KEY env var is required')
  process.exit(1)
}

const apps = process.argv.slice(2)
if (apps.length === 0) {
  console.error('usage: node scripts/fetch-unsplash.mjs <app-dir> [...more]')
  process.exit(1)
}

const sleep = (ms) => new Promise((r) => setTimeout(r, ms))
const usedPhotoIds = new Set()

async function search(query, count) {
  const url = `https://api.unsplash.com/search/photos?query=${encodeURIComponent(query)}&per_page=${Math.min(count + 4, 30)}&orientation=squarish&content_filter=high`
  const res = await fetch(url, { headers: { Authorization: `Client-ID ${ACCESS_KEY}` } })
  if (!res.ok) throw new Error(`Unsplash ${res.status} for "${query}": ${await res.text()}`)
  const remaining = res.headers.get('x-ratelimit-remaining')
  const data = await res.json()
  const fresh = data.results.filter((p) => !usedPhotoIds.has(p.id))
  const picked = (fresh.length >= count ? fresh : data.results).slice(0, count)
  picked.forEach((p) => usedPhotoIds.add(p.id))
  console.log(`  "${query}" → ${picked.length}/${count} photos (rate remaining: ${remaining})`)
  return picked
}

function cropUrl(photo, w, h) {
  return `${photo.urls.raw}&w=${w}&h=${h}&fit=crop&crop=entropy&q=80&auto=format`
}

for (const appDir of apps) {
  const dir = resolve(process.cwd(), appDir)
  const manifestPath = resolve(dir, 'src/images.manifest.mjs')
  const { manifest } = await import(pathToFileURL(manifestPath).href)
  console.log(`\n${appDir} — ${manifest.length} entries`)

  const images = {}
  for (const entry of manifest) {
    const keys = entry.keys ?? [entry.key]
    const photos = await search(entry.query, keys.length)
    keys.forEach((key, i) => {
      const photo = photos[i % photos.length]
      if (!photo) {
        console.warn(`  ! no photo for "${key}" (${entry.query})`)
        return
      }
      images[key] = {
        url: cropUrl(photo, entry.w ?? 900, entry.h ?? 900),
        alt: photo.alt_description ?? entry.query,
        credit: { name: photo.user.name, link: photo.user.links.html },
      }
    })
    await sleep(350)
  }

  const header =
    `// generated by fayz-sdk/scripts/fetch-unsplash.mjs — do not edit by hand.\n` +
    `// Photos via Unsplash (https://unsplash.com). Credits embedded per image.\n`
  const body = `export const IMAGES: Record<string, { url: string; alt: string; credit: { name: string; link: string } }> = ${JSON.stringify(images, null, 2)}\n`
  const outPath = resolve(dir, 'src/images.generated.ts')
  writeFileSync(outPath, header + body)
  console.log(`  wrote ${outPath} (${Object.keys(images).length} images)`)
}
